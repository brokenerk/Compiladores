import math

def Sin(x):
    return math.sin( float(x) )

def Cos(x):
    return math.cos( float(x) )

def Atan(x):
    return math.atan( float(x) )

def Log(x):
    return math.log( float(x) )

def Log10(x):
    return math.log10( float(x) )

def Exp(x):
    return math.exp( float(x) )

def Sqrt(x):
    return math.sqrt( float(x) )

def Int(x):
    return int ( x )

def Abs(x):
    return math.fabs( float(x) )